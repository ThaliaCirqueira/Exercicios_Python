
# 03 Crie duas variáveis num1 e num2 e atribua valores diferentes. Verifique se num1 é igual a num2. Se forem iguais, exiba "Números iguais", caso contrário, exiba "Números diferentes".

numero1 = float(input("Insira o primeiro número: "))
numero2 = float(input("Insira o Segundo número: "))

if numero1 == numero2:
    print("Números iguais")
else:
    print("Números diferentes")

